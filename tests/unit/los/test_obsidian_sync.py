def test_sync_marks_done_items_in_db(conn: sqlite3.Connection) -> None:
    """Items marked [x] in file but open in DB must be set to status='done'.

    Only items already in DB are affected — done_count only counts transitions
    from open→done, not [x] items that didn't exist in DB.
    """
    # Insert "Call the dentist" as open — it appears [x] in the file
    row_id = insert_action_item(conn, text="Call the dentist", source="telegram", source_message_id=None)

    result = sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)

    item = get_item_by_id(conn, row_id)
    assert item.status == ActionItemStatus.DONE
    assert item.done_at is not None
    # Only 1 done transition: "Call the dentist" was open in DB; "Connect Obsidian to Lobster"
    # was not in DB at all so no transition counted
    assert result.done_count == 1


def test_sync_sets_done_at_timestamp(conn: sqlite3.Connection) -> None:
    """mark_done must set done_at to a non-empty ISO timestamp."""
    row_id = insert_action_item(conn, text="Call the dentist", source="telegram", source_message_id=None)
    sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)
    item = get_item_by_id(conn, row_id)
    assert item.done_at is not None
    assert "T" in item.done_at  # ISO-8601 format


def test_sync_does_not_reopen_already_done_items(conn: sqlite3.Connection) -> None:
    """An item already done in DB that appears open in file must NOT be reopened."""
    row_id = insert_action_item(conn, text="Book flight Boston to Miami", source="telegram", source_message_id=None)
    mark_done(conn, row_id)

    # "Book flight Boston to Miami" is [ ] (open) in the file
    sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)

    item = get_item_by_id(conn, row_id)
    # Must remain done — the file says open but DB has authoritative done status
    assert item.status == ActionItemStatus.DONE


