def test_sync_result_summary_counts_are_accurate(conn: sqlite3.Connection) -> None:
    """SyncResult must accurately report done/inserted/priority_changed counts."""
    # Pre-insert one item at wrong priority so priority_changed fires
    # "Book flight Boston to Miami" is in Urgent section (P1-P3), inserting at priority=8
    row_id = insert_action_item(
        conn, text="Book flight Boston to Miami", source="telegram", source_message_id=None, priority=8
    )
    # Pre-insert "Call the dentist" as open — it appears [x] in file, so should be marked done
    row_dentist = insert_action_item(conn, text="Call the dentist", source="telegram", source_message_id=None)

    result = sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)

    # "Call the dentist" was open → should be marked done (1 transition)
    assert result.done_count == 1
    assert get_item_by_id(conn, row_dentist).status == ActionItemStatus.DONE
    # "Complete FC work items", "Time box Lobster stuff", "Learn Rust" are new open items not in DB
    assert result.inserted_count == 3
    # "Book flight" was at wrong priority band (8 vs urgent max 3)
    assert result.priority_changed_count == 1


