def test_dedup_key_matches_db_implementation() -> None:
    """dedup_key in sync must use the same algorithm as src.los.db.compute_dedup_key."""
    # The sync script imports compute_dedup_key directly from src.los.db.
    # This test verifies the expected normalization — lowercase, strip punctuation, collapse whitespace.
    key = compute_dedup_key("Call the dentist!")
    assert key == compute_dedup_key("CALL THE DENTIST")
    assert len(key) == 16
    assert key == compute_dedup_key("call   the  dentist")


def test_dedup_key_is_deterministic() -> None:
    """Same text always produces the same key."""
    text = "Complete FC work items"
    assert compute_dedup_key(text) == compute_dedup_key(text)


def test_dedup_key_differs_for_different_text() -> None:
    """Different texts produce different keys."""
    assert compute_dedup_key("Book flight") != compute_dedup_key("Learn Rust")


