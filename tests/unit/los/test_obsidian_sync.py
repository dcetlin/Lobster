def test_sync_updates_priority_when_item_moves_sections(conn: sqlite3.Connection) -> None:
    """If an item exists in DB with different priority than its file section, update priority."""
    # Insert "Book flight Boston to Miami" as low priority (7) but it's in Urgent section (P1-P3)
    row_id = insert_action_item(
        conn, text="Book flight Boston to Miami", source="telegram", source_message_id=None, priority=7
    )
    item_before = get_item_by_id(conn, row_id)
    assert item_before.priority == 7

    sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)

    item_after = get_item_by_id(conn, row_id)
    assert item_after.priority <= PRIORITY_URGENT_MAX, (
        f"Priority should have been updated to urgent range, got {item_after.priority}"
    )
    # Verify priority actually changed
    assert item_after.priority != 7


def test_sync_does_not_change_priority_when_item_already_in_same_band(conn: sqlite3.Connection) -> None:
    """If an item is already in the same priority band as its file section, leave it alone."""
    # Insert "Learn Rust" with priority 8 (someday band = P7–P9)
    row_id = insert_action_item(
        conn, text="Learn Rust", source="telegram", source_message_id=None, priority=8
    )

    sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)

    item_after = get_item_by_id(conn, row_id)
    assert item_after.priority == 8


# ---------------------------------------------------------------------------
# sync_obsidian_to_db — idempotency
# ---------------------------------------------------------------------------


def test_sync_is_idempotent(conn: sqlite3.Connection) -> None:
    """Running sync twice must produce the same DB state as running it once."""
    result1 = sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)
    result2 = sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)

    # No new inserts on second run — all items already in DB
    assert result2.inserted_count == 0

    # Total row count must be the same after both runs
    cur = conn.execute("SELECT COUNT(*) FROM action_items")
    count = cur.fetchone()[0]
    assert count == result1.inserted_count + result1.done_count


def test_sync_idempotent_done_status(conn: sqlite3.Connection) -> None:
    """Running sync twice does not create duplicate done transitions."""
    row_id = insert_action_item(conn, text="Call the dentist", source="telegram", source_message_id=None)

    sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)
    first_done_at = get_item_by_id(conn, row_id).done_at

    sync_obsidian_to_db(conn, SAMPLE_ACTIVE_TODOS)
    second_done_at = get_item_by_id(conn, row_id).done_at

    # done_at should not be overwritten on second pass (item is already done)
    assert first_done_at == second_done_at


